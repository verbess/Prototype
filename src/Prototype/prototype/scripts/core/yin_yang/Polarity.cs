namespace YinYang;

/// <summary>
/// Specifies the polarity of yin yang elements.
/// </summary>
public enum Polarity
{
    /// <summary>
    /// The yin element, when qi is negative.
    /// </summary>
    Yin = -1,

    /// <summary>
    /// The tai ji element, when qi is in the neutral balance.
    /// </summary>
    TaiJi = 0,

    /// <summary>
    /// The yang element, when qi is positive.
    /// </summary>
    Yang = 1,
}
