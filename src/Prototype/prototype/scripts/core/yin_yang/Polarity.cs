namespace Yinyang;

/// <summary>
/// Specifies the polarity of yinyang elements.
/// </summary>
public enum Polarity
{
    /// <summary>
    /// The yin element, when qi is negative.
    /// </summary>
    Yin = -1,

    /// <summary>
    /// The taiji element, when qi is in the neutral balance.
    /// </summary>
    Taiji = 0,

    /// <summary>
    /// The yang element, when qi is positive.
    /// </summary>
    Yang = 1,
}
